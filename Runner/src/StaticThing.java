import javafx.scene.image.ImageView;

public class StaticThing {
    private double sizeX;
    private double sizeY;
    private ImageView image;

    public StaticThing(double sizeX,double sizeY, ImageView image) {
        this.sizeX = sizeX;
        this.sizeY = sizeY;
        this.image = image;
    }
}
